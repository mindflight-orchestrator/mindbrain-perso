#!/usr/bin/env node
/**
 * Generate schema_column_manifest.generated.zig from sql/sqlite_mindbrain--1.0.0.sql.
 * Run from mindbrain-perso root: node tools/gen-schema-column-manifest.mjs
 */

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const sqlPath = join(root, "sql", "sqlite_mindbrain--1.0.0.sql");
const outPath = join(
  root,
  "src",
  "standalone",
  "schema_column_manifest.generated.zig"
);

const sql = readFileSync(sqlPath, "utf8");

const TABLE_START =
  /CREATE TABLE IF NOT EXISTS\s+([A-Za-z_][A-Za-z0-9_]*)\s*\(/gi;

function extractTableBlocks(source) {
  const blocks = [];
  let match;
  while ((match = TABLE_START.exec(source)) !== null) {
    const table = match[1];
    let depth = 1;
    let i = TABLE_START.lastIndex;
    const bodyStart = i;
    while (i < source.length && depth > 0) {
      const ch = source[i];
      if (ch === "(") depth += 1;
      else if (ch === ")") depth -= 1;
      i += 1;
    }
    if (depth !== 0) continue;
    const body = source.slice(bodyStart, i - 1);
    blocks.push({ table, body });
    TABLE_START.lastIndex = i;
  }
  return blocks;
}

function splitBodyLines(body) {
  const lines = [];
  let current = "";
  let depth = 0;
  for (let i = 0; i < body.length; i += 1) {
    const ch = body[i];
    if (ch === "(") depth += 1;
    if (ch === ")") depth -= 1;
    if (ch === "," && depth === 0) {
      lines.push(current.trim());
      current = "";
      continue;
    }
    current += ch;
  }
  if (current.trim()) lines.push(current.trim());
  return lines;
}

function isTableLevelConstraint(line) {
  const upper = line.toUpperCase();
  return (
    upper.startsWith("PRIMARY KEY") ||
    upper.startsWith("FOREIGN KEY") ||
    upper.startsWith("UNIQUE(") ||
    upper.startsWith("UNIQUE (") ||
    upper.startsWith("CHECK(") ||
    upper.startsWith("CHECK (") ||
    upper.startsWith("CONSTRAINT ")
  );
}

function sanitizeDdlForAlter(ddl) {
  let out = ddl.replace(/\s+/g, " ").trim();
  out = out.replace(/\s+REFERENCES\b[\s\S]*$/i, "").trim();
  out = out.replace(/\s+PRIMARY KEY\b/i, "").trim();
  out = out.replace(/\s+UNIQUE\b(?!\()/i, "").trim();
  return out;
}

function migrationId(table, column) {
  return `add-col-${table}-${column}`;
}

const entries = [];

for (const { table, body } of extractTableBlocks(sql)) {
  for (const line of splitBodyLines(body)) {
    if (!line || isTableLevelConstraint(line)) continue;
    const m = /^([A-Za-z_][A-Za-z0-9_]*)\s+([\s\S]+)$/.exec(line);
    if (!m) continue;
    const column = m[1];
    const ddl = sanitizeDdlForAlter(m[2]);
    if (!ddl || /\bPRIMARY KEY\b/i.test(m[2])) continue;
    entries.push({ table, column, ddl, migrationId: migrationId(table, column) });
  }
}

entries.sort((a, b) =>
  a.table === b.table
    ? a.column.localeCompare(b.column)
    : a.table.localeCompare(b.table)
);

const lines = [
  "// Auto-generated by tools/gen-schema-column-manifest.mjs — do not edit.",
  "// Source: sql/sqlite_mindbrain--1.0.0.sql",
  "",
  "pub const ColumnManifestEntry = struct {",
  "    table: []const u8,",
  "    column: []const u8,",
  "    ddl: []const u8,",
  "    migration_id: []const u8,",
  "};",
  "",
  "pub const column_manifest: []const ColumnManifestEntry = &.{" 
];

for (const e of entries) {
  const esc = (s) => s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
  lines.push(
    `    .{ .table = "${esc(e.table)}", .column = "${esc(e.column)}", .ddl = "${esc(e.ddl)}", .migration_id = "${esc(e.migrationId)}" },`
  );
}

lines.push("};", "", `pub const column_manifest_count = ${entries.length};`, "");

writeFileSync(outPath, `${lines.join("\n")}\n`);
console.log(`Wrote ${entries.length} column entries to ${outPath}`);
